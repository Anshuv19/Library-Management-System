public void getIssueBookDetails(){
    
      
    int bookId = Integer.parseInt(jCTextField10.getText());
    int student_Id = Integer.parseInt(jCTextField11.getText());
     
    try {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library_ms","root","2868");
        String sql = "select *from issuesbook where book_id =? and student_id = ? and status = ?" ;
        
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setInt(1, bookId);
        pst.setInt(2, student_Id);
        pst.setString(3, "pending");
       
        
        ResultSet rs = pst.executeQuery();
        
        while(rs.next()){
            
             lblissueid.setText(rs.getString("id"));
             lblbookname.setText(rs.getString("book_name"));
             lblstudentname.setText(rs.getString("student_name"));
             lblssuedate.setText(rs.getString("issue_date"));
             lblduedate.setText(rs.getString("due_date"));
        }
        
            
        
        
    } catch (Exception e) {
        e.printStackTrace();
    }
      
}   

