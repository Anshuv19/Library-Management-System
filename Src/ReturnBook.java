public boolean returnBook(){
    boolean isReturned = false;
    int bookId = Integer.parseInt(jCTextField10.getText());
    int student_Id = Integer.parseInt(jCTextField11.getText());
    
    try {
         Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library_ms","root","2868");
        String sql = "update issuesbook set status = ? where student_id = ? and book_id=? and status = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, "returned");
        pst.setInt(2, bookId);
        pst.setInt(3, student_Id);
        pst.setString(4, "pending");
        
        int rowCount = pst.executeUpdate();
        if(rowCount>0){
            isReturned = true;
        }else{
            isReturned = false;
        }
        
    } catch (Exception e) {
        e.printStackTrace();
    }
      return isReturned;
}

